txt = 'Hello World'
print(txt)

print("Hitung jumlah karakternya: ",len(txt))
print("Ambil karakter terakhir: ",txt[-1])
print("Ambil karakter index ke-2 sampai index ke-4 (llo): ",txt[2:5])
print("Hilangkan spasi pada text tersebut (HelloWorld): ",txt.replace(" ",""))
print("Ubah text menjadi huruf besar: ",txt.upper())
print ("Ubah text menjadi huruf kecil: ",txt.lower())
print ("Ganti karakter H dengan karakter J: ",txt.replace('H','J'))